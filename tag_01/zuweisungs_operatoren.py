# Zuweisungsoperatoren

meine_variable = 4

meine_variable += 3     # meine_variable = meine_variable + 3
meine_variable -= 1     # meine_variable = meine_variable - 1
print(meine_variable)

meine_variable /= 2
meine_variable *= 2
meine_variable %= 2
meine_variable **= 2
meine_variable //= 3

print(meine_variable)